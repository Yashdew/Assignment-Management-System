    private void jButtonSumbitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSumbitActionPerformed
        // TODO add your handling code here: SAVE
        String id=jLabelIDnumber.getText();
        String Fullname=jTextFieldFullName.getText();
        String Email=jTextFieldEmail.getText();
        String Password1=jPasswordFieldPassword.getText();
        String Rollno=jTextFieldRollNo.getText();
        
        String Division=(String)jComboBoxDiv.getSelectedItem();
        String Year1=(String)jComboBoxYear.getSelectedItem();
        String Brach=(String)jComboBoxBranch.getSelectedItem();
        
        try
        {
            Connection con=ConnectionProvider.getCon();
            PreparedStatement ps=con.prepareStatement("insert into studentinfo values (?,?,?, ?,?,?, ?,?)");
            ps.setString(1, id);
            ps.setString(2, Fullname);
            ps.setString(3, Email);
            ps.setString(4, Password1);
            ps.setString(5, Rollno);
            ps.setString(6, Division);
            ps.setString(7, Year1);
            ps.setString(8, Brach);
            
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null,"Successfully Register,Now you can Login");
            setVisible(false);
            new StudentLogin().setVisible(true);
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButtonSumbitActionPerformed

